    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        
        //O usuário seleciona pesquisa por espécie
        if (combobox.getSelectedIndex() == 0) {
            if (!txtDigiteODadoReferenteAoItemEscolhido.getText().trim().isEmpty()) {

                DocumentoDAO pesquisa = new DocumentoDAO();
                System.out.println(txtDigiteODadoReferenteAoItemEscolhido.getText());
                resultado = pesquisa.buscarPorEspecie(txtDigiteODadoReferenteAoItemEscolhido.getText());
                
                if (!resultado.isEmpty()) {
                    TotalPesquisado.setText(""+resultado.size()+" documento(s) encontrados(s)");
                    TotalPesquisado.setFont(new java.awt.Font("Tahoma", 1, 14));
                    contadorExibicao = 0;
                    gerarExibicao(resultado.get(0));
                    contadorExibicao++;

                } else {
                    JOptionPane.showMessageDialog(this, "Não há documentos cadastrados com esta espécie");
                }

            } else {
                JOptionPane.showMessageDialog(this, "Dado de busca não informado");
            }


        }

        //O usuário seleciona pesquisa por data inicial
        if (combobox.getSelectedIndex() == 1) {
            if (!txtDigiteODadoReferenteAoItemEscolhido.getText().trim().isEmpty()) {
                DocumentoDAO pesquisa = new DocumentoDAO();
                resultado = pesquisa.buscarPorDataInicial(txtDigiteODadoReferenteAoItemEscolhido.getText());

                if (!resultado.isEmpty()) {
                    TotalPesquisado.setText(""+resultado.size()+" documento(s) encontrados(s)");
                    TotalPesquisado.setFont(new java.awt.Font("Tahoma", 1, 14));
                    contadorExibicao = 0;
                    gerarExibicao(resultado.get(0));
                    contadorExibicao++;

                } else {
                    JOptionPane.showMessageDialog(this, "Não há documentos para esta data inicial. Certifique-se do preenchimento dd/mm/aaaa");
                }

            } else {
                JOptionPane.showMessageDialog(this, "Dado de busca não informado");
            }


        }

        //O usuário seleciona pesquisa por data final
        if (combobox.getSelectedIndex() == 2) {
            if (!txtDigiteODadoReferenteAoItemEscolhido.getText().trim().isEmpty()) {
                DocumentoDAO pesquisa = new DocumentoDAO();
                resultado = pesquisa.buscarPorDataFinal(txtDigiteODadoReferenteAoItemEscolhido.getText());

                if (!resultado.isEmpty()) {
                    TotalPesquisado.setText(""+resultado.size()+" documento(s) encontrados(s)");
                    TotalPesquisado.setFont(new java.awt.Font("Tahoma", 1, 14));
                    contadorExibicao = 0;
                    gerarExibicao(resultado.get(0));
                    contadorExibicao++;

                } else {
                    JOptionPane.showMessageDialog(this, "Não há documentos para esta data final. Certifique-se do preenchimento dd/mm/aaaa");
                }

            } else {
                JOptionPane.showMessageDialog(this, "Dado de busca não informado");
            }
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

