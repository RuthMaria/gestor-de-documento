    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        documentoAtualizado = resultado.get(contadorExibicao - 1);
        documentoAtualizado.setConservacao(txtConservacao.getText());
        documentoAtualizado.setDataFinal(txtDataFinal.getText());
        documentoAtualizado.setDataInicial(txtDataInicial.getText());
        documentoAtualizado.setDescricao(txtDescricoes.getText());
        documentoAtualizado.setEspecie(txtEspecie.getText());
        documentoAtualizado.setObservacao(txtObservacoes.getText());
        documentoAtualizado.setOrganizacao(txtOrganizacao.getText());
        documentoAtualizado.setPreenchimento(txtRevisao.getText());
        documentoAtualizado.setProveniencia(txtproveniencia.getText());
        documentoAtualizado.setQuantidade(txtQuantidade.getText());
        documentoAtualizado.setRevisao(txtResponsavel.getText());
        documentoAtualizado.setIdentificacaoDocumento(txtPreenchimento.getText());
        documentoAtualizado.setImagem(txtImagem.getText());

        documentoAtualizado.setUltimoUsuario(usuario);

        try {
            DocumentoDAO dDAO = new DocumentoDAO();
            dDAO.atualizar(documentoAtualizado);
            JOptionPane.showMessageDialog(this, "Documento Atualizado");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "O documento não pode ser atualizado");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    
    private void proximoDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proximoDocActionPerformed
        
        if (resultado.size() != contadorExibicao) {
            gerarExibicao(resultado.get(contadorExibicao));
            contadorExibicao++;
            
        } else {
            contadorExibicao = 0;
            gerarExibicao(resultado.get(contadorExibicao));
            contadorExibicao++;
        }
    }//GEN-LAST:event_proximoDocActionPerformed

    
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        try {
            Diretorio d = new Diretorio();
            d.visible();
            diretorios = d.pegarDiretorio();

            for (int i = 0; i < diretorios.length; i++) {
                txtImagem.setText(txtImagem.getText().concat(diretorios[i].toString()).concat(";"));
            }

        } catch (Exception ex) {
            Logger.getLogger(CadastrarDocumento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    
