    private void proximoDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proximoDocActionPerformed
        
        if (resultado.size() != contadorExibicao) {
            gerarExibicao(resultado.get(contadorExibicao));
            contadorExibicao++;
            
        } else {
            contadorExibicao = 0;
            gerarExibicao(resultado.get(contadorExibicao));
            contadorExibicao++;
        }
    }//GEN-LAST:event_proximoDocActionPerformed

