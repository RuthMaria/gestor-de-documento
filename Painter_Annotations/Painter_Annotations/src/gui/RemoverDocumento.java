    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        
        documentoRemovido = resultado.get(contadorExibicao - 1);
        
        try {
            DocumentoDAO dDAO = new DocumentoDAO();
            dDAO.remover(documentoRemovido);
            limparTela();
            JOptionPane.showMessageDialog(this, "Documento Removido!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "O documento não pode ser removido");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

