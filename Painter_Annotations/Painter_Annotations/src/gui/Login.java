     private void onClickSalvar() throws Exception {
        try {
            UsuarioDAO uDAO = new UsuarioDAO();
            Usuario us = uDAO.recuperarPeloLogin(txtUsuario.getText());
            
            if (us.getSenha().equals(txtSenha.getText())) {
                autenticado = true;
                usuario = us;
                
            } else {
                autenticado = false;
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao conectar com o banco!\n" + e.getLocalizedMessage());
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, e);
        }
    }
 
    
